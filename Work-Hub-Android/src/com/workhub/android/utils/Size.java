package com.workhub.android.utils;

public class Size {

	public int width;
	public int height;
	
	public Size(int width, int height) {
		super();
		this.width = width;
		this.height = height;
	}

	@Override
	public String toString() {
		return "Size [height=" + height + ", width=" + width + "]";
	}
	
	
	
}
